-- 10. DimDate
-- Generate 24 months: Jan 2023 to Dec 2024
-- Run this script to populate DimDate
-- ============================================

DECLARE @StartDate DATE = '2023-01-01'
DECLARE @EndDate   DATE = '2024-12-31'
DECLARE @Date DATE = @StartDate

WHILE @Date <= @EndDate
BEGIN
    INSERT INTO DimDate (
        DateKey, Date, Day, Month, MonthName,
        Quarter, Year, WeekDay, WeekDayNumber,
        IsWeekend, WeekOfYear, FiscalPeriod
    )
    VALUES (
        CAST(FORMAT(@Date, 'yyyyMMdd') AS INT),
        @Date,
        DAY(@Date),
        MONTH(@Date),
        DATENAME(MONTH, @Date),
        DATEPART(QUARTER, @Date),
        YEAR(@Date),
        DATENAME(WEEKDAY, @Date),
        DATEPART(WEEKDAY, @Date),
        CASE WHEN DATEPART(WEEKDAY, @Date) IN (1,7) THEN 1 ELSE 0 END,
        DATEPART(WEEK, @Date),
        'FY' + CAST(YEAR(@Date) AS VARCHAR) + '-Q' + CAST(DATEPART(QUARTER,@Date) AS VARCHAR)
    )
    SET @Date = DATEADD(DAY, 1, @Date)
END

PRINT 'DimDate populated: ' + CAST(DATEDIFF(DAY,'2023-01-01','2024-12-31')+1 AS VARCHAR) + ' rows'
